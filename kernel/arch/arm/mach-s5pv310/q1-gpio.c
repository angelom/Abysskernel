void c1_config_gpio_table(void)
{
	u32 i, gpio;

	printk(KERN_DEBUG "c1_config_gpio_table.\n");

	for (i = 0; i < ARRAY_SIZE(q1_init_gpios); i++) {
		gpio = q1_init_gpios[i].num;
		if (gpio <= S5PV310_MP02(5)) {
			s3c_gpio_cfgpin(gpio, q1_init_gpios[i].cfg);
			s3c_gpio_setpull(gpio, q1_init_gpios[i].pud);

			if (q1_init_gpios[i].val != S3C_GPIO_SETPIN_NONE)
				gpio_set_value(gpio, q1_init_gpios[i].val);

			s5p_gpio_set_drvstr(gpio, q1_init_gpios[i].drv);
		}
	}

	/* Initialize GPF0-2 */
	for (i = 0; i < 8; i++) {
		s3c_gpio_slp_cfgpin(S5PV310_GPF0(i), S3C_GPIO_SLP_INPUT);
		s3c_gpio_slp_setpull_updown(S5PV310_GPF0(i), S3C_GPIO_PULL_DOWN);
		s3c_gpio_slp_cfgpin(S5PV310_GPF1(i), S3C_GPIO_SLP_INPUT);
		s3c_gpio_slp_setpull_updown(S5PV310_GPF1(i), S3C_GPIO_PULL_DOWN);
		s3c_gpio_slp_cfgpin(S5PV310_GPF2(i), S3C_GPIO_SLP_INPUT);
		s3c_gpio_slp_setpull_updown(S5PV310_GPF2(i), S3C_GPIO_PULL_DOWN);
	}
}

