static void config_sleep_gpio_table(int array_size, unsigned int (*gpio_table)[3])
{
	u32 i, gpio;

	for (i = 0; i < array_size; i++) {
		gpio = gpio_table[i][0];
		s3c_gpio_slp_cfgpin(gpio, gpio_table[i][1]);
		s3c_gpio_slp_setpull_updown(gpio, gpio_table[i][2]);
	}

	/* GPX GPIO setting */
	gpio_request(S5PV310_GPX1(7), "GPX17");
	gpio_direction_input(S5PV310_GPX1(7));
	s3c_gpio_setpull(S5PV310_GPX1(7), S3C_GPIO_PULL_DOWN);
	gpio_free(S5PV310_GPX1(7));
}

